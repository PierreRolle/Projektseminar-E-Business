import MainMenuView from "./views/MainMenuView.js";
import ManualView from "./views/ManualView.js";
import GameView from "./views/GameView.js";
import startGame from "./game/GameState.js";
import EndView from "./views/EndView.js";
import ManualView2 from "./views/ManualView2.js";

const pathToRegex = (path) =>
  new RegExp("^" + path.replace(/\//g, "\\/").replace(/:\w+/g, "(.+)") + "$");

const getParams = (match) => {
  const values = match.result.slice(1);
  const keys = Array.from(match.route.path.matchAll(/:(\w+)/g)).map(
    (result) => result[1]
  );

  return Object.fromEntries(
    keys.map((key, i) => {
      return [key, values[i]];
    })
  );
};

const navigateTo = (url) => {
  history.pushState(null, null, url);
  router();
};

const router = async () => {
  const routes = [
    { path: "/", view: MainMenuView },
    { path: "/mainMenu", view: MainMenuView },
    { path: "/manual", view: ManualView },
    { path: "/game", view: GameView },
    { path: "/end", view: EndView },
    { path: "/manual2", view: ManualView2 },
  ];

  // Test each route for potential match
  const potentialMatches = routes.map((route) => {
    return {
      route: route,
      result: location.pathname.match(pathToRegex(route.path)),
    };
  });

  let match = potentialMatches.find(
    (potentialMatch) => potentialMatch.result !== null
  );

  if (!match) {
    match = {
      route: routes[0],
      result: [location.pathname],
    };
  }

  const view = new match.route.view(getParams(match));

  document.querySelector("#app").innerHTML = await view.getHtml();

  if (view.path == "/game") {
    await view.getHtml().then(() => {
      (() => {
        Array.from(Array(1000).keys()).map((e) => {
          clearInterval(e);
        });
        window.removeEventListener("keydown", window.keyHandler);
      })();
      startGame();
    });
  }
};

window.addEventListener("popstate", router);

document.addEventListener("DOMContentLoaded", () => {
  document.body.addEventListener("click", (e) => {
    if (e.target.matches("[data-link]")) {
      e.preventDefault();
      navigateTo(e.target.href);
    }
  });

  router();
});
